/* File names: dropdown_modified_v0_0_2.js   Version date:  Author: Rachel Bush
   File description: This is a version of Bootstrap's dropdown.js which has been modified to add a .mutable class.
   The file itself had to be modified because the class was implemented by modifying the clearMenus function, which isn't assigned to a variable.
   The .mutable indicates that the main visible change caused by clicking the option will occur in the dropdown, rather than the webpage.
   The class goes on an <li> in .dropdown-menu, but it can also go on .dropdown-menu itself when all the options have this kind of effect.
   For example, when the dropdown's main effects are on the server, the only visible change will be in the text of the dropdown option.
   In this case, we wouldn't want to hide the dropdown until the user has had time to observe the changes.
   The area inside .mutable is exempt from the effect where clicking anywhere on the document closes the dropdown.
   Version description:  */

